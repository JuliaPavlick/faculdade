# Declarando variáveis de tipos diferentes
nome = 'Ana' # str
idade = 20 # int
altura = 1.65 # float
ativo = True # bool
# Verificando o tipo de cada variável
print(type(nome)) # <class 'str'>
print(type(idade)) # <class 'int'>
print(type(altura)) # <class 'float'>
print(type(ativo)) # <class 'bool'>

# A saída no terminal mostra o tipo de cada variável usando a função type().
# Cada linha exibida (<class 'str'>, <class 'int'>, etc.) representa o tipo
# de dado da variável correspondente.
# O Python utiliza esse formato padrão "<class 'tipo'>" para indicar o tipo.
# Importante: essa saída não mostra o valor da variável, apenas sua classificação (tipo).